	/// <summary>
	/// Information about an individual Service Charge.
	/// </summary>
	public class ServiceChargeItem
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	Source																																*
		//*-----------------------------------------------------------------------*
		private string mSource = "";
		/// <summary>
		/// Get/Set the command source string for this instance.
		/// </summary>
		public string Source
		{
			get { return mSource; }
			set { mSource = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	Value																																	*
		//*-----------------------------------------------------------------------*
		private decimal mValue = 0;
		/// <summary>
		/// Get/Set the Calculated Value of the Service Charge.
		/// </summary>
		public decimal Value
		{
			get { return mValue; }
			set { mValue = value; }
		}
		//*-----------------------------------------------------------------------*

	}
