using System;
using System.Collections;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace BankViewModel
{
	//*-------------------------------------------------------------------------*
	//*	BankModelCollection																											*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Collection of ObservableCollection<T> Items.
	/// </summary>
	public class BankModelCollection : List<BankModelItem>
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************

		//*-----------------------------------------------------------------------*
		//*	Add																																		*
		//*-----------------------------------------------------------------------*
		/// <summary>
		/// Add a new item to the collection by member values.
		/// </summary>
		public BankModelItem Add(string name, IEnumerable items)
		{
			BankModelItem result = new BankModelItem();
			result.Name = name;
			result.Table = items;
			this.Add(result);
			return result;
		}
		//*-----------------------------------------------------------------------*


	}
	//*-------------------------------------------------------------------------*

	//*-------------------------------------------------------------------------*
	//*	BankModelItem																														*
	//*-------------------------------------------------------------------------*
	/// <summary>
	/// Information about items loaded in a table.
	/// </summary>
	public class BankModelItem
	{
		//*************************************************************************
		//*	Private																																*
		//*************************************************************************
		//*************************************************************************
		//*	Protected																															*
		//*************************************************************************
		//*************************************************************************
		//*	Public																																*
		//*************************************************************************
		//*-----------------------------------------------------------------------*
		//*	Name																																	*
		//*-----------------------------------------------------------------------*
		private string mName = "";
		/// <summary>
		/// Get/Set the Name of this table.
		/// </summary>
		public string Name
		{
			get { return mName; }
			set { mName = value; }
		}
		//*-----------------------------------------------------------------------*

		//*-----------------------------------------------------------------------*
		//*	Table																																	*
		//*-----------------------------------------------------------------------*
		private IEnumerable mTable = null;
		/// <summary>
		/// Get/Set a reference to the collection of items.
		/// </summary>
		public IEnumerable Table
		{
			get { return mTable; }
			set { mTable = value; }
		}
		//*-----------------------------------------------------------------------*

	}
	//*-------------------------------------------------------------------------*


}
